import { NextIntlClientProvider, useMessages } from "next-intl";

import { Footer } from "@/components/Footer";
import { Header } from "@/components/Header";
import { Toaster } from "@/components/notifications";
import { NotoSans } from "@/fontLoader.ts";
import { css } from "~/styled-system/css";
import { Grid } from "~/styled-system/jsx";

import "../globals.css";

export const metadata = {
  title: "Next.js",
  description: "Generated by Next.js",
};

export default function RootLayout({
  children,
  params: { locale },
}: {
  children: React.ReactNode;
  params: {
    locale: string;
  };
}) {
  const messages = useMessages();

  return (
    <html lang={locale} className={`${NotoSans.variable}`}>
      <NextIntlClientProvider locale={locale} messages={messages}>
        <body className={css({ fontFamily: "noto" })}>
          <Grid gridTemplateRows="auto 1fr auto" gap="0" minHeight="full">
            <Header />
            <main>{children}</main>
            <Toaster />
            <Footer />
          </Grid>
        </body>
      </NextIntlClientProvider>
    </html>
  );
}
